import math

akar = math.sqrt(36)
pangkat = math.pow(3,4)
bulat_bawah = math.floor(8.4)
bulat_atas = math.ceil(8.4)
print()
print(f"akar 36 = {akar}")
print(f"3 pangkat 4 = {pangkat}")
print(f"Pembulatan ke bawah dari 8.4 = {bulat_bawah}") 
print(f"Pembulatan ke atas dari 8.4 = {bulat_atas}")
print()